            <h2>SOBRE</h2>
            <h3>
                A quadrilha, dança típica das festas juninas brasileiras, é carregada de referências caipiras e matutas. Mas sua origem vem de muito longe. A “quadrille” surgiu em Paris, no século XVIII, como uma dança de salão composta por quatro casais. Era dançada pela elite europeia e veio para o Brasil durante o período da Regência (por volta de 1830), onde era febre no ambiente aristocrático.
                Da Corte carioca, a quadrilha acabou caindo no gosto do povo. Ao longo do século XIX, a dança se popularizou no Brasil e se fundiu com manifestações brasileiras preexistentes. “O brasileiro é um povo muito criativo e criou a forma estilizada de dançar a dança dos nobres”, opina a arte-educadora Lucinaide Pinheiro. A partir daí, diversas evoluções foram sendo incorporadas à quadrilha, entre elas o aumento do número de pares dançantes e o abandono de passos e ritmos franceses. As músicas e o casamento caipira que antecede a dança, também foram novidades incorporadas ao longo dos anos.</h3><br>
